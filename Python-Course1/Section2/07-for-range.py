for i in range(1, 20):
    print(f"i is now {i}")

print(30 * "* " + "Range Step" + 30 * " *")

# Even element
for i in range(0, 20, 2):
    print(f"i is now {i}")

print()

# Odd element
for i in range(1, 20, 2):
    print(f"i is now {i}")

print()

# Backward Even element
for i in range(18, 0, -2):
    print(f"i is now {i}")

print()

# Backward Odd element
for i in range(19, 0, -2):
    print(f"i is now {i}")